    private void buttonInserirNaListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonInserirNaListaActionPerformed
        if (selectProdutos.getSelectedIndex() != 0 && selectClientes.getSelectedIndex() != 0) {
            String produto = (String) selectProdutos.getSelectedItem();
            String cliente = (String) selectClientes.getSelectedItem();
            String quantidade = textQuantidade.getText();
            
            if (!produto.isBlank() && !produto.isEmpty() && !cliente.isBlank() && !cliente.isEmpty() && !quantidade.isBlank() && !quantidade.isEmpty()) {
                clienteVenda = lojaVirtual.pesquisarCliente(cliente);

                String[] produtosInfo = produto.split("-");
                Produto p = lojaVirtual.pesquisarProduto(produtosInfo[0].trim());
                listaProdutosVenda.add(p);                

                total += Integer.parseInt(quantidade) * p.getPreco();

                labelTotal.setText("Total:" + " " + String.valueOf(total));
                labelClienteInfo.setText(labelClienteInfo.getText() + " " + cliente);

                list.addElement("Produto: " + produto + ", Quantidade: " + quantidade);

                listaProdutosSelecionados.setModel(list);
            } else {
                JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente e/ou produto!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonInserirNaListaActionPerformed

    private void buttonFinalizarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonFinalizarCompraActionPerformed
        int opcao = JOptionPane.showConfirmDialog(null, "Finalizar a venda?", "Atenção!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (opcao == 0) {
            Venda v = new Venda();
            v.setCliente(clienteVenda);
            v.setProdutos(listaProdutosVenda);
            v.setTotal_pagar(total);

            lojaVirtual.getVendas().add(v);
            
            limparCampos();
        }
    }//GEN-LAST:event_buttonFinalizarCompraActionPerformed

