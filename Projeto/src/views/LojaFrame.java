    private void limparCampos() {
        list.removeAllElements();
        labelTotal.setText("Total:");
        labelClienteInfo.setText("Produtos selecionados para");
        textQuantidade.setText("");
        selectProdutos.setSelectedIndex(0);
        selectClientes.setSelectedIndex(0);
    }
